        versionName = flutter.versionName
        
        // Load API key from .env file
        val envFile = rootProject.file("../frontend/.env")
        if (envFile.exists()) {
            val envProperties = Properties()
            envProperties.load(FileInputStream(envFile))
            manifestPlaceholders["GOOGLE_CLOUD_API_KEY"] = envProperties.getProperty("GOOGLE_CLOUD_API_KEY", "")
        } else {
            manifestPlaceholders["GOOGLE_CLOUD_API_KEY"] = ""
        }
